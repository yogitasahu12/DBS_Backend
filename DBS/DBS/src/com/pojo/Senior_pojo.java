package com.pojo;

public class Senior_pojo {
	private int Roll_no;
	private String S_Name;
	private int S_year;
	private String Area_Interest;
	private String Branch;
	private String Gender;
	private String email_id;
	public int getRoll_no() {
		return Roll_no;
	}
	public void setRoll_no(int roll_no) {
		Roll_no = roll_no;
	}
	public String getS_Name() {
		return S_Name;
	}
	public void setS_name(String s_name) {
		S_Name = s_name;
	}
	public int getS_year() {
		return S_year;
	}
	public void setS_year(int s_year) {
		S_year = s_year;
	}
	public String getArea_Interest() {
		return Area_Interest;
	}
	public void setArea_Interest(String area_Interest) {
		Area_Interest = area_Interest;
	}
	public String getBranch() {
		return Branch;
	}
	public void setBranch(String branch) {
		Branch = branch;
	}
	public String getGender() {
		return Gender;
	}
	public void setGender(String gender) {
		Gender = gender;
	}
	public String getEmail_id() {
		return email_id;
	}
	public void setEmail_id(String email_id) {
		this.email_id = email_id;
	}
	
}
